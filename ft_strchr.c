#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	k;

	k = 0;
	while (s[k])
	{
		if (s[k] == '\0')
			return (NULL);
		if (s[k] == (char)c)
			return ((char)&s[k + 1]);
		k++;
	}
}
